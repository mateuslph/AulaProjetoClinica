    private void jBsalvarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarMedicoActionPerformed
        mod.setNome(jTnomeMedico.getText());
        mod.setEspecialidade((String) jCespecialidadeMedico.getSelectedItem());
        mod.setCrm(Integer.parseInt(jFcrmMedico.getText()));
        control.Salvar(mod);
        jTnomeMedico.setText("");
        jFcrmMedico.setText("");
        jTnomeMedico.setEnabled(false);
        jFcrmMedico.setEnabled(false);
        jCespecialidadeMedico.setEnabled(false);
        jBsalvarMedico.setEnabled(false);
    }//GEN-LAST:event_jBsalvarMedicoActionPerformed

