    private void jBpesquisaMedicosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBpesquisaMedicosActionPerformed
        mod.setPesquisa(jTpesqusaMedico.getText());
        BeansMedico model = control.buscaMedico(mod);
        jTnomeMedico.setText(model.getNome());
        jFcrmMedico.setText(String.valueOf(model.getCrm()));
        jCespecialidadeMedico.setSelectedItem(model.getEspecialidade());
        jBeditarMedico.setEnabled(true);
        jBexcluirMedico.setEnabled(true);
    }//GEN-LAST:event_jBpesquisaMedicosActionPerformed

